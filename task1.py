# Целочисленная
integer_var = 23

# Вещественная
float_var = 2.6

# Логическая
bool_var = True

# Строковая
string_var = "Hello, world!"

# Пустая
none_var = None

# Вывод переменных и их типов
print("Целочисленная переменная:", integer_var, "Тип:", type(integer_var))
print("Вещественная переменная:", float_var, "Тип:", type(float_var))
print("Логическая переменная:", bool_var, "Тип:", type(bool_var))
print("Строковая переменная:", string_var, "Тип:", type(string_var))
print("Переменная пустого типа:", none_var, "Тип:", type(none_var))